local pluginKeys = {}


pluginKeys.cmp = function(cmp)
  return {
    -- 上一个
    ["<C-p>"] = cmp.mapping.select_prev_item(),
    -- 下一个
    ["<C-n>"] = cmp.mapping.select_next_item(),
    -- 选择补全
    ["<Tab>"] = cmp.mapping.confirm(),
    --  出现或关闭补全
    ["<C-o>"] = cmp.mapping(
      {
        i = function()
          if cmp.visible() then
            cmp.abort()
          else
            cmp.complete()
          end
        end,
        c = function()
          if cmp.visible() then
            cmp.close()
          else
            cmp.complete()
          end
        end
      }
    ),
    -- 类似于 IDEA 的功能，如果没进入选择框，tab
    -- 会选择下一个，如果进入了选择框，tab 会确认当前选择
    ["<Tab>"] = cmp.mapping(
      function(fallback)
        if cmp.visible() then
          local entry = cmp.get_selected_entry()
          if not entry then
            cmp.select_next_item({ behavior = cmp.SelectBehavior.Select })
          end
          cmp.confirm()
        else
          fallback()
        end
      end,
      { "i", "s", "c" }
    )
  }
end

